import java.io.*;
import java.util.*;

class Even_Practice {
    public static void main(String[] args) {

        int i = 0;
        while (i <= 20) {
            System.out.println(i);

            if (i % 2 == 0) {
                System.out.println("It is Even Number=" + i);

            }
            i++;
        }

    }
}